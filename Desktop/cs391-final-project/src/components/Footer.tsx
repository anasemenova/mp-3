// my component
export default function Footer() {
    // set at 0 so that first slide is shown initially
    const [index, setIndex] = useState(0);

    // updates the index state when arrows are clicked
    const handleSelect = (selectedIndex: number) => {
        setIndex(selectedIndex);
    };

    /*
    Part of the following code is from the website mentioned above.
    I added the "mapping" over the tips array.
     */
    return (
        <FooterDiv>
            <Carousel activeIndex={index} onSelect={handleSelect}>
                {tips.map((tip, item) => (
                    <Carousel.Item key={item}>
                        <SlideContent />
                        <Carousel.Caption>
                            <h3>{tip.title}</h3>
                            <p>{tip.description}</p>
                        </Carousel.Caption>
                    </Carousel.Item>
                ))}
            </Carousel>
        </FooterDiv>
    );
}
